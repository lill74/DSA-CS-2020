# 문자들을 모아 놓았음으로 문자 '열' (List)

string = "Lee Soul"
for letter in string:
  print(letter)

# Inside 'list' books each of A B C is called 'Element'
books = ["A", "B", "C"]
for book in books:
  print(book)
  
for i in range(5):
  print("Lee")
  print(i)
  
# Range and skip : reversed for-loop
for i in range(10, 5, -1):
  print(i)
